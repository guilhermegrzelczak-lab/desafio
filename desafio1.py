#soma dos numeros

#pede os dois numeros ao usuario 
num1 = int(input('diga o primeiro numero da soma: '))
num2 = int(input('diga o segundo numero da soma: '))

#cria uma variavel para guardar a soma dos numeros
soma = num1 + num2

#retorna a soma dos numeros ao usuario
print(f"{num1} + {num2} é igual a: {soma}")